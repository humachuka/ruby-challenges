weather = 'foggy'

case weather 
when 'sunny'
	puts "Wear nothing! It's too hot for clothes."
when 'foggy'
	puts "Wear a hat with flashlights so you can see!"
when 'cloudy'
	puts "Wear whatever people where in Seattle."
when 'rainy'
	puts "Wear some rad rain boots!"

else 
	puts "Wear whatever you want. It's a free country!"
		
end